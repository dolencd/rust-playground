use std::ops::Add;

const SEAFLOOR_WIDTH: usize = 100;
const SEAFLOOR_HEIGHT: usize = 100;

pub struct Seafloor {
    positions: [[u8; SEAFLOOR_WIDTH]; SEAFLOOR_HEIGHT],
}

pub struct Basin {
    positions: Vec<Position>,
}

impl Basin {
    pub fn len(&self) -> usize {
        self.positions.len()
    }
}

impl PartialEq for Basin {
    fn eq(&self, other: &Self) -> bool {
        self.positions.iter().any(|own_position| {
            other
                .positions
                .iter()
                .any(|other_position| own_position == other_position)
        })
    }
}

#[derive(Debug, Clone, Copy)]
pub struct Position(i32, i32);

impl Add for Position {
    fn add(self, rhs: Self) -> Self::Output {
        Position(self.0 + rhs.0, self.1 + rhs.1)
    }
    type Output = Position;
}

impl PartialEq for Position {
    fn eq(&self, other: &Self) -> bool {
        self.0 == other.0 && self.1 == other.1
    }
}

impl Seafloor {
    pub fn get_pos(&self, pos: &Position) -> Option<u8> {
        Some(
            self.positions
                .get(pos.0 as usize)?
                .get(pos.1 as usize)?
                .to_owned(),
        )
    }

    pub fn get_adjacent(&self, pos: &Position) -> Vec<(Position, u8)> {
        vec![
            pos.to_owned() + Position(0, 1),
            pos.to_owned() + Position(-1, 0),
            pos.to_owned() + Position(1, 0),
            pos.to_owned() + Position(0, -1),
        ]
        .iter()
        .filter_map(|pos_to_check| Some((pos_to_check.to_owned(), self.get_pos(pos_to_check)?)))
        .collect()
    }

    pub fn get_all_elements(&self) -> impl Iterator<Item = (Position, &u8)> {
        self.positions
            .iter()
            .enumerate()
            .flat_map(|(current_row, row_array)| {
                row_array
                    .iter()
                    .enumerate()
                    .map(move |(current_column, row_element)| {
                        (
                            Position(
                                current_row.try_into().unwrap(),
                                current_column.try_into().unwrap(),
                            ),
                            row_element,
                        )
                    })
            })
    }

    fn fill_basin_positions(&self, position_arr: &mut Vec<Position>, position_to_check: Position) {
        if position_arr.iter().any(|pos| position_to_check == *pos) {
            return;
        }

        if self.get_pos(&position_to_check).unwrap() == 9 {
            return;
        }

        position_arr.push(position_to_check);

        for adjacent_position in (self.get_adjacent(&position_to_check)).iter() {
            self.fill_basin_positions(position_arr, adjacent_position.0);
        }
    }

    pub fn get_basin_with_low_point(&self, low_point: &Position) -> Basin {
        let mut basin_positions = vec![];
        self.fill_basin_positions(&mut basin_positions, low_point.to_owned());
        Basin {
            positions: basin_positions,
        }
    }
}

